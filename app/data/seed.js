export async function seedDatabase() {
    try {
        console.log('Starting database seed...');

        // Insert seed data
        for (const contact of seedData) {
            await sql`
                INSERT INTO contacts (
                    id, first_name, last_name, avatar, twitter, notes, 
                    favorite, email, phone, job, active, created_at, updated_at
                ) VALUES (
                    ${contact.id}, 
                    ${contact.first_name}, 
                    ${contact.last_name}, 
                    ${contact.avatar || null}, 
                    ${contact.twitter || null}, 
                    ${contact.notes || null}, 
                    ${contact.favorite || false}, 
                    ${contact.email || null}, 
                    ${contact.phone || null}, 
                    ${contact.job || null}, 
                    ${contact.active ?? true},
                    ${contact.created_at || new Date().toISOString()},
                    ${contact.updated_at || new Date().toISOString()}
                )
                ON CONFLICT (id) DO NOTHING
            `;
        }

        console.log(`Seeded ${seedData.length} contacts successfully!`);
    } catch (error) {
        console.error('Error seeding database:', error);
        throw error;
    }
}

seedDatabase()
    .then(() => {
        console.log('Seeding completed!');
        process.exit(0);
    })
    .catch((error) => {
        console.error('Seeding failed:', error);
        process.exit(1);
    });